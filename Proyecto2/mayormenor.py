def mayor(a,b):
    """funcion que entre dos valor enteros devuelve el mayor

    Args:
        a (int): valor ingresado 1
        b (int): valor ingresado 2

    Returns:
        valor mayor: despues de un condicional se define el mayor y se devuelve
    """
    if a > b:
        return a
    else:
        return b

